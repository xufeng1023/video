    {
        $this->validate($request, [
            'post_id' => 'required|exists:posts,id',
            'images' => 'required|array'
        ]);

        foreach($request->images as $image) {
            Image::create([
                'post_id' => $request->post_id,
                'slug' => $slugs[] = $image->store('upload', 'public')
            ]);
